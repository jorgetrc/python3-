print(1234)

# Aspas simples
print('Texto exemplo')

# Aspas duplas
print("Texto exemplo")

# Escape - Protege caracteres especiais para que sejam impressos com print.
print("Texto \"exemplo\"")
print('Texto "exemplo"')

# r - Mostra todos os caracteres do print inclusive especiais.
print(r"Texto \"exemplo\"")